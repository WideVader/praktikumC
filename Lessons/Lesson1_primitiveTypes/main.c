#include <stdio.h>

int main() {
    int a = 10;
    printf("Integer %d \n", a);

    long b = 100000l;
    printf("Long %ld \n", b);

    float c = 1.3f;
    printf("Float %.2f \n", c);

    double d = 1.3;
    printf("Double %.2lf \n", d);

    char e = 'l';
    printf("Char %c \n", e);

    char f = 48;
    printf("Char %c \n", f);
}